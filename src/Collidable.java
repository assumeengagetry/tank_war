import java.awt.Rectangle;

/**
 * Collidable接口定义了可碰撞对象的行为契约
 * 
 * 这个接口展示了OOP中的以下概念：
 * 1. 接口（Interface）：
 *    - 接口是一种完全抽象的类型
 *    - 定义了一组方法但不实现它们
 *    - 实现类必须提供所有方法的具体实现
 * 
 * 2. 多态（Polymorphism）：
 *    - 不同的类可以实现同一个接口
 *    - 每个类可以有自己独特的碰撞处理逻辑
 *    - 在运行时，系统会调用对象实际类型的方法
 * 
 * 3. 契约编程：
 *    - 接口定义了一个"契约"，规定了实现类必须提供的功能
 *    - 这种方式使得代码更容易维护和扩展
 */
public interface Collidable {
    /**
     * 获取对象的碰撞边界
     * 这个方法用于碰撞检测，每个可碰撞的对象都必须定义自己的边界
     * @return 返回一个Rectangle对象，表示该对象在游戏中的碰撞边界
     */
    Rectangle getBounds();
    
    /**
     * 处理与其他游戏对象的碰撞
     * 当两个对象发生碰撞时，这个方法将被调用
     * 不同类型的对象可以有不同的碰撞处理逻辑，这体现了多态
     * @param other 与之发生碰撞的另一个游戏对象
     */
    void handleCollision(GameObject other);
    
    /**
     * 判断对象当前是否可以发生碰撞
     * 某些情况下，对象可能暂时不参与碰撞检测
     * 例如：物体已经被销毁、处于无敌状态等
     * @return 如果对象当前可以发生碰撞则返回true，否则返回false
     */
    boolean isCollidable();
}
